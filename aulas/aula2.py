## Tipos de Dados

print(1234) #int
print(-10) #int
print(1.74)#Float
print(10.11, -10.11)#Float
print("Gabriel") #String com as Aspas Duplas
print('Resende') #String com as Aspas Simples
print("Gabriel 'Resende'") #Aspas simples usada para colocar aspas no nome
print("Gabriel \"Resende\"") #Escape para utilizar aspas em uma String

print(type('Gabriel'))
print(type(123))
print(type(12.30))
print(type(True)) #Tipo Boolean
print(type(False)) #Tipo Boolean

print(10 == 10) #metodo boolean True "==" usado para comparação
print(10 == 15) #metodo boolean False "==" usado para comparação

#Conversão de tipos

print(int('1') + 1)
print(1.5 + float(2))

